##คำสั่ง print คำตอบจะอยู่แถวเดียวกันและติดกัน
for i in range(3):
    print(i,end="")

##คำสั่ง print ธรรมดา คำตอบจะขึ้นทีละแถว
for i in range(3):
    print(i)

##คำสั่ง print คำตอบจะอยู่แถวเดียวกันและห่างกัน 1 วรรค
for i in range(3):
    print(i,end=" ")

##คำสั่ง print คำตอบจะอยู่แถวเดียวกันและมี -
for i in range(3):
    print(i, end="-")